package com.sf.bean;

public class NetWorkMeter {
	private Integer netWorkMeterID;
	private Integer netWorkID;
	private Integer meterID;
	private Integer meterStyle;
	private Integer orderIndex;
	private String meterType;
	public Integer getNetWorkMeterID() {
		return netWorkMeterID;
	}
	public void setNetWorkMeterID(Integer netWorkMeterID) {
		this.netWorkMeterID = netWorkMeterID;
	}
	public Integer getNetWorkID() {
		return netWorkID;
	}
	public void setNetWorkID(Integer netWorkID) {
		this.netWorkID = netWorkID;
	}
	public Integer getMeterID() {
		return meterID;
	}
	public void setMeterID(Integer meterID) {
		this.meterID = meterID;
	}
	public Integer getMeterStyle() {
		return meterStyle;
	}
	public void setMeterStyle(Integer meterStyle) {
		this.meterStyle = meterStyle;
	}
	public Integer getOrderIndex() {
		return orderIndex;
	}
	public void setOrderIndex(Integer orderIndex) {
		this.orderIndex = orderIndex;
	}
	public String getMeterType() {
		return meterType;
	}
	public void setMeterType(String meterType) {
		this.meterType = meterType;
	}
	
}
