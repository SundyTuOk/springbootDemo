package com.sf.bean;

public class DictionaryValue {
  
	private Integer dictionaryValueID;
	private Integer dictionaryID;
	private String dictionaryValueNum;
	private String dictionaryValueValue;
	public Integer getDictionaryValueID() {
		return dictionaryValueID;
	}
	public void setDictionaryValueID(Integer dictionaryValueID) {
		this.dictionaryValueID = dictionaryValueID;
	}
	public Integer getDictionaryID() {
		return dictionaryID;
	}
	public void setDictionaryID(Integer dictionaryID) {
		this.dictionaryID = dictionaryID;
	}
	public String getDictionaryValueNum() {
		return dictionaryValueNum;
	}
	public void setDictionaryValueNum(String dictionaryValueNum) {
		this.dictionaryValueNum = dictionaryValueNum;
	}
	public String getDictionaryValueValue() {
		return dictionaryValueValue;
	}
	public void setDictionaryValueValue(String dictionaryValueValue) {
		this.dictionaryValueValue = dictionaryValueValue;
	}
	
	
	
}
